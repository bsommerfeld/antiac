package de.bsommerfeld.antiac.click;

/**
 * Defines the types of click events that can occur.
 * This enumeration is used to specify the nature of a click,
 * such as an attack action in the context of a system that processes
 * click events.
 */
public enum ClickType {
  /**
   * Represents a click event of type "ATTACK".
   * This type indicates that the click action corresponds to an attack
   * in the context of a system that processes click events.
   */
  ATTACK,
}
